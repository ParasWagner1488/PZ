'''Вариант 27.

1. Даны числа x, y. Проверить истинность высказывания: «Точка с координатами (x, y) лежит во второй или третьей координатной четверти».'''

def check():
    print("=" * 60)
    print("ЗАДАНИЕ 1: Проверка положения точки в координатных четвертях")
    print("=" * 60)
    
    try:
        # Ввод координат
        x = float(input("Введите координату x: "))
        y = float(input("Введите координату y: "))
        
        print(f"\nКоординаты точки: ({x}, {y})")
        print("-" * 40)
        
        # Определение координатной четверти
        if x > 0 and y > 0:
            quarter = "первой"
            result = False
        elif x < 0 and y > 0:
            quarter = "второй"
            result = True
        elif x < 0 and y < 0:
            quarter = "третьей"
            result = True
        elif x > 0 and y < 0:
            quarter = "четвертой"
            result = False
        else:
            # Оси координат
            if x == 0 and y == 0:
                quarter = "начало координат"
            elif x == 0:
                quarter = "оси Y"
            else:
                quarter = "оси X"
            result = False
        
        # Вывод результатов
        print(f"Точка находится в {quarter} координатной четверти")
        print(f"Утверждение 'Точка лежит во второй или третьей четверти': {result}")
        
        # Дополнительная информация о четвертях
        print("\nСправка по координатным четвертям:")
        print("I четверть: x > 0, y > 0")
        print("II четверть: x < 0, y > 0")
        print("III четверть: x < 0, y < 0") 
        print("IV четверть: x > 0, y < 0")
        
    except ValueError:
        print("Ошибка! Введите числовые значения для координат.")

check()

