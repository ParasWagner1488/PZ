'''Задача: Вариант 27 Дано целое число N (количество секунд), нужно найти сколько секунд прошло с начала последнего часа.'''
def s(N):
    return N % 3600

if __name__ == "__main__":
    test_values = [3600, 3661, 7200, 123, 59, 3601]
    
    for N in test_values:
        result = s(N)
        print(f"N = {N} секунд -> {result} секунд с начала последнего часа")
        
    try:
        N = int(input("\n Введите количество секунд N: "))
        result = s(N)
        print(f"С начала последнего часа прошло: {result} секунд")
    except ValueError:
        print("Ошибка: введите целое число")
