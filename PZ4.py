'''Вариант 27.

1. Дано целое число N (>0). Найти сумму 1 + 1/2 + 1/3 + ... + 1/N'''

# Задание 1
def task1():
    print("=" * 40)
    print("СУММА РЯДА 1 + 1/2 + 1/3 + ... + 1/N")
    print("=" * 40)
    
    try:
        N = int(input("Введите N (>0): "))
        
        if N <= 0:
            print("Ошибка! N должно быть > 0")
            return
        
        S = 0
        for i in range(1, N + 1):
            S += 1 / i
        
        print(f"Сумма ряда: {S:.6f}")
        
    except:
        print("Ошибка ввода!")

'''
2. Дано целое число N (>1). Вывести наименьшее из целых чисел K, для которых
сумма 1 + 2 + ... + K будет больше или равна N, и саму эту сумму.
'''

# Задание 2
def task2():
    print("\n" + "=" * 50)
    print("НАИМЕНЬШЕЕ K, ГДЕ СУММА 1+2+...+K >= N")
    print("=" * 50)
    
    try:
        N = int(input("Введите N (>1): "))
        
        if N <= 1:
            print("Ошибка! N должно быть > 1")
            return
        
        K = 0
        S = 0
        while S < N:
            K += 1
            S += K
        
        print(f"Наименьшее K: {K}")
        print(f"Сумма 1+2+...+{K} = {S}")
        
    except:
        print("Ошибка ввода!")

# Запуск заданий
task1()
task2()
